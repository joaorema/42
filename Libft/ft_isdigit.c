#include "libft.h"

int ft_isdigit(int c)
{
	if(c < 0 || c > 127)  //confirmar se baste ate 127
	{
		return 0;
	}
	if(c >= '0' && c <= '9')
	{
		return 1;
	}
	return (0);
}
